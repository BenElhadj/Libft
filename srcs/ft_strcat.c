#include "libft.h"

char	*ft_strcat(char *dest, const char *src)
{
	size_t size;
	size_t i;

	size = 0;
	i = 0;
	while (dest[size])
		size++;
	while (src[i])
	{
		dest[size + i] = src[i];
		i++;
	}
	dest[size + i] = '\0';
	return (dest);
}
