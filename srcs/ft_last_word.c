#include "libft.h"

void	ft_last_word(char *str)
{
	int	i;

	i = (ft_strlen(str) - 1);
	while ((str[i] == ' ' || str[i] == '\t') && i >= 0)
		i--;
	while (str[i] > ' ' && str[i] <= 'z')
		i--;
	i++;
	while (str[i] > ' ' && str[i] <= 'z')
	{
		write(1, &str[i], 1);
		i++;
	}
}
